import joblib
import pandas as pd
import numpy as np
from lime.lime_text import LimeTextExplainer
from sklearn.pipeline import make_pipeline
from IPython.display import display, HTML

# 1. GEREKLİ DOSYALARI YÜKLE
model = joblib.load('/content/drive/MyDrive/linearsvm_model_trained.joblib')
vectorizer = joblib.load('/content/drive/MyDrive/tfidf_model.joblib')
common_errors = pd.read_csv('/content/drive/MyDrive/tum_modeller_ortak_hatalar.csv')

# 2. LIME İÇİN PİPELINE VE YARDIMCI FONKSİYON
c = make_pipeline(vectorizer, model)

# LinearSVM tek sütunlu karar üretir ama binary classificationda LIME iki sütunlu karar bekler.
# Burda bu dönüşüm yapılır.
def svm_predict_proba(texts):
    decision_scores = c.decision_function(texts)
    # LIME'ın beklediği [P(negatif), P(pozitif)] yapısı
    return np.vstack([-decision_scores, decision_scores]).T

# 3. LIME KURULUMU
class_names = ['İnsan', 'AI']
explainer = LimeTextExplainer(class_names=class_names)

# 4. İLK İKİ HATAYI DÖNGÜ İLE İNCELEME İŞLEMİ YAPILIYOR.
for idx in range(2):
    sample_text = str(common_errors.iloc[idx]['clean_text'])
    true_label = int(common_errors.iloc[idx]['generated'])

    print(f"\n{'='*50}")
    print(f"ÖRNEK ANALİZİ #{idx+1}")
    print(f"GERÇEK ETİKET: {class_names[true_label]}")
    print(f"{'='*50}")

    # LIME Açıklaması
    exp = explainer.explain_instance(
        sample_text,
        svm_predict_proba,
        num_features=10,
        labels=(1,)
    )

    # Görselleştirmeyi notebook içinde göster
    exp.show_in_notebook(text=True)
