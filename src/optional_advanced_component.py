def get_error_stats(model, name):
    preds = model.predict(X_test_vec)
    errors = X_test_pandas[preds != y_test].copy()

    # Hata türlerini ayırıyoruz. (False Positive / False Negative)
    # 0: İnsan, 1: AI
    fp = errors[(errors['generated'] == 0) & (preds[preds != y_test] == 1)] # İnsana AI demiş
    fn = errors[(errors['generated'] == 1) & (preds[preds != y_test] == 0)] # AI'ya İnsan demiş

    print(f"\n=== {name} HATA ANALİZİ ===")
    print(f"Toplam Hata: {len(errors)}")
    print(f"İnsan olduğu halde AI denilen (False Positive): {len(fp)}")
    print(f"AI olduğu halde İnsan denilen (False Negative): {len(fn)}")
    return errors

# 3. Karşılaştırmalı sonuçları alıyoruz.
