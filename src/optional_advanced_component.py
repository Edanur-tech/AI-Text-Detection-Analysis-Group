def plot_svc_feature_importance(tfidf_vectorizer, model, n_top=20):
    # 1. Kelime isimlerini (features) al
    feature_names = tfidf_vectorizer.get_feature_names_out()

    # 2. LinearSVC katsayılarını al (LinearSVC'de model.coef_[0] içindedir)
    coefficients = model.coef_[0]

    # 3. Bir DataFrame oluşturup sırala
    feature_importance = pd.DataFrame({'feature': feature_names, 'coef': coefficients})

    # AI tarafı için en yüksek katsayılar (Pozitif)
    top_ai = feature_importance.sort_values(by='coef', ascending=False).head(n_top)

    # İnsan tarafı için en düşük katsayılar (Negatif)
    top_human = feature_importance.sort_values(by='coef', ascending=True).head(n_top)

    # 4. Görselleştirme
    plt.figure(figsize=(15, 8))

    # AI Grafiği
    plt.subplot(1, 2, 1)
    plt.barh(top_ai['feature'], top_ai['coef'], color='salmon')
    plt.xlabel("Katsayı (Pozitif -> AI)")
    plt.title(f"AI Metni İçin En Belirleyici {n_top} Kelime")
    plt.gca().invert_yaxis() # En önemliler en üstte görünsün

    # İnsan Grafiği
    plt.subplot(1, 2, 2)
    plt.barh(top_human['feature'], top_human['coef'], color='skyblue')
    plt.xlabel("Katsayı (Negatif -> İnsan)")
    plt.title(f"İnsan Metni İçin En Belirleyici {n_top} Kelime")
    plt.gca().invert_yaxis()

    plt.tight_layout()
    plt.show()

# Fonksiyonu çalıştır
plot_svc_feature_importance(tfidf, model)

