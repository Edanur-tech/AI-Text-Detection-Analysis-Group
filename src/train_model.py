# 3. Data Preprocessing Aşamasında tanımladığımız'final_preprocess' fonksiyonunu kullanıyoruz.
def final_preprocess_for_test(text):
    if not text: return ""
    # Lowercase & Punctuations Removal
    text = str(text).lower()
    text = re.sub(r'[^a-z\s]', '', text)
    # Tokenization & Stop_word Removal
    tokens = [w for w in text.split() if w not in stop_words]
    return " ".join(tokens)

