import joblib
import re
import nltk
from nltk.corpus import stopwords

# 1. Gerekli altyapıyı hazırlıyoruz.
nltk.download('stopwords')
stop_words = set(stopwords.words('english'))

# Özel ayarlarımızı koruyoruz: 'not' ve 'no' stop_word listesinden çıkarılıyor, 'students' ve 'people' ise ekleniyor.
if 'not' in stop_words: stop_words.remove('not')
if 'no' in stop_words: stop_words.remove('no')
stop_words.update(['students', 'people'])

# 2. Vektör ve model yükleniyor.
print("Vektör ve model yükleniyor!")
tfidf = joblib.load('/content/drive/MyDrive/tfidf_model.joblib')
